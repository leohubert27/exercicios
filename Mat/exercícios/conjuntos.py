conjuntoA = {1,3,6,7,8}
conjuntoB = {1,2,5,7,9}

print("Conjunto A: ", conjuntoA)
print("Conjunto B: ", conjuntoB)

#União
print("A união dos conjuntos é: ",conjuntoA|conjuntoB) 

#Interseção
print("A interseção dos conjuntos é: ", conjuntoA & conjuntoB)

#Diferença A - B
print("A diferença dos conjuntos A e B é: ", conjuntoA - conjuntoB)

#Diferença B - A
print("A diferença dos conjuntos B e A é: ", conjuntoB - conjuntoA)

#Diferença Simétrica
print("A diferença simétrica entre os conjuntos é: ", conjuntoA ^ conjuntoB)